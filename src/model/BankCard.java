package model;

import java.util.Scanner;

public class BankCard {
    private String cardNumber;
    private int onePassword;
    private int replenishment;

    public int getOnePassword() {
        return onePassword;
    }

    public int getReplenishment() {
        return replenishment;
    }

    public String getCardNumber() {
        return cardNumber;
    }

    public void setCardNumber(String cardNumber) {
        this.cardNumber = cardNumber;
    }

    public void setOnePassword(int onePassword) {
        this.onePassword = onePassword;
    }

    public void setReplenishment(int replenishment) {
        this.replenishment = replenishment;
    }










}
